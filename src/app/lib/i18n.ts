export function t(key: TranslationKey, lang: Language): string {
  return translations[lang][key] || translations['de'][key] || key;
}

export function getLanguage(): Language {
  if (typeof window === 'undefined') return 'de';
  const saved = localStorage.getItem('timetracker_language') as Language | null;
  if (saved && (saved === 'de' || saved === 'en' || saved === 'es' || saved === 'fr' || saved === 'ru')) {
    return saved;
  }
  return 'de';
}

export function setLanguage(lang: Language): void {
  if (typeof window === 'undefined') return;
  localStorage.setItem('timetracker_language', lang);
}

export const allLanguages: Language[] = ['de', 'en', 'es', 'fr', 'ru'];

// React Hook for components
export function useI18n() {
  const [lang, setLangState] = useState<Language>('de');

  useEffect(() => {
    setLangState(getLanguage());
  }, []);

  const setLang = useCallback((newLang: Language) => {
    setLanguage(newLang);
    setLangState(newLang);
  }, []);

  const translate = useCallback((key: TranslationKey) => {
    return t(key, lang);
  }, [lang]);

  return { lang, setLang, t: translate };
}
